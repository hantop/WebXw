package app.creditapp.cif.entity;

public class CifPersRelLine {

	private String idNo;
	private String cifName;
	private String rel;
	private String relIdNo;
	private String relCifName;
	
	public String getIdNo() {
		return idNo;
	}
	public void setIdNo(String idNo) {
		this.idNo = idNo;
	}
	public String getCifName() {
		return cifName;
	}
	public void setCifName(String cifName) {
		this.cifName = cifName;
	}
	public String getRel() {
		return rel;
	}
	public void setRel(String rel) {
		this.rel = rel;
	}
	public String getRelIdNo() {
		return relIdNo;
	}
	public void setRelIdNo(String relIdNo) {
		this.relIdNo = relIdNo;
	}
	public String getRelCifName() {
		return relCifName;
	}
	public void setRelCifName(String relCifName) {
		this.relCifName = relCifName;
	}
	
}
